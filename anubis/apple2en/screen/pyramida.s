	ext	PYRAMIDA_str100
	ext	PYRAMIDA_str200
	ext	PYRAMIDA_str300
	ext	PYRAMIDA_str400
	ext	PYRAMIDA_str500
	ext	PYRAMIDA_str700
	ext	PYRAMIDA_str800
	ext	PYRAMIDA_str900
	ext	PYRAMIDA_str1000

PYRAMIDA	@LOAD	#pPYRAMIDA
PYRAMIDA_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMIDA_str100
	@PRINT	#0;PYRAMIDA_str200

PYRAMIDA_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMIDA_200
	jmp	PYRAMID9
PYRAMIDA_200	cmp	#'2'
	bne	PYRAMIDA_210
	jmp	PYRAMIDB
PYRAMIDA_210	cmp	#'3'
	bne	PYRAMIDA_220
	jmp	PYRAMIDA_300
PYRAMIDA_220	cmp	#'4'
	bne	PYRAMIDA_230
	jmp	PYRAMIDA_400
PYRAMIDA_230	cmp	#'5'
	bne	PYRAMIDA_240
	jmp	PYRAMIDA_600
PYRAMIDA_240	cmp	#'6'
	bne	PYRAMIDA_LOOP	
	jmp	PYRAMIDA_500

PYRAMIDA_300	@PRINT	#0;PYRAMIDA_str300
	@INKEY
	jmp	PYRAMIDA_60

PYRAMIDA_400	@PRINT	#0;PYRAMIDA_str400
	@INKEY
	jmp	PYRAMIDC

PYRAMIDA_500	@PRINT	#0;PYRAMIDA_str500
	@INKEY
	jmp	MORT

PYRAMIDA_600	lda	PEEK_7003
	cmp	#0
	beq	PYRAMIDA_700
	cmp	#1
	beq	PYRAMIDA_800
	cmp	#2
	beq	PYRAMIDA_900
	cmp	#3
	beq	PYRAMIDA_1000
	cmp	#4
	beq	PYRAMIDA_1000
	
PYRAMIDA_700	@PRINT	#0;PYRAMIDA_str700
	@INKEY
	lda	#1
	sta	PEEK_7003
	jmp	PYRAMIDA_60

PYRAMIDA_800	@PRINT	#0;PYRAMIDA_str800
	@INKEY
	lda	#2
	sta	PEEK_7003
	jmp	PYRAMIDA_60

PYRAMIDA_900	@PRINT	#0;PYRAMIDA_str900
	@INKEY
	lda	#3
	sta	PEEK_7003
	jmp	PYRAMIDA_60

PYRAMIDA_1000	@PRINT	#0;PYRAMIDA_str1000
	@INKEY
	lda	#4
	sta	PEEK_7003
	jmp	PYRAMIDA_60

*---

pPYRAMIDA	strl	'@/data/pyramida.lz4'
