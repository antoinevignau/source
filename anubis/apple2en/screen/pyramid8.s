PYRAMID8	@LOAD	#pPYRAMID8
PYRAMID8_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID8_str100
	@PRINT	#0;PYRAMID8_str200

PYRAMID8_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID8_200
	jmp	PYRAMID7
PYRAMID8_200	cmp	#'2'
	bne	PYRAMID8_210
	jmp	PYRAMID8_300
PYRAMID8_210	cmp	#'3'
	bne	PYRAMID8_220
	jmp	PYRAMID8_400
PYRAMID8_220	cmp	#'4'
	bne	PYRAMID8_230
	jmp	PYRAMID8_500
PYRAMID8_230	cmp	#'5'
	bne	PYRAMID8_240
	jmp	PYRAMID8_600
PYRAMID8_240	cmp	#'6'
	bne	PYRAMID8_LOOP	

	lda	PEEK_7003
	cmp	#4
	bcc	PYRAMID8_700
	bcs	PYRAMID8_800

*---

PYRAMID8_300	@PRINT	#0;PYRAMID8_str300
	@INKEY
	jmp	PYRAMID8_60

PYRAMID8_400	@PRINT	#0;PYRAMID8_str400
	@INKEY
	jmp	PYRAMID8_60

PYRAMID8_500	@PRINT	#0;PYRAMID8_str500
	@INKEY
	jmp	PYRAMID8_60

PYRAMID8_600	@PRINT	#0;PYRAMID8_str600
	@INKEY
	jmp	MORT

PYRAMID8_700	@PRINT	#0;PYRAMID8_str700
	@INKEY
	jmp	PYRAMID8_60

PYRAMID8_800	@PRINT	#0;PYRAMID8_str800
	@INKEY
	@PRINT	#0;PYRAMID8_str990
	@INKEY
	@PRINT	#0;PYRAMID8_str1020
	@INKEY
	jmp	PYRAMID8_60

