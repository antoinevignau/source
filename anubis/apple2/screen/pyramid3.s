	ext	PYRAMID3_str100
	ext	PYRAMID3_str200
	ext	PYRAMID3_str300
	ext	PYRAMID3_str400
	ext	PYRAMID3_str480
	ext	PYRAMID3_str527
	ext	PYRAMID3_str590
	ext	PYRAMID3_str700
	ext	PYRAMID3_str760
	ext	PYRAMID3_str810
	ext	PYRAMID3_str860
	ext	PYRAMID3_speech480
	ext	PYRAMID3_speech481
	ext	PYRAMID3_speech527
	ext	PYRAMID3_speech528
	ext	PYRAMID3_speech529
	ext	PYRAMID3_speech530

PYRAMID3	@LOAD	#pPYRAMID3
PYRAMID3_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID3_str100
	@PRINT	#0;PYRAMID3_str200

PYRAMID3_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID3_200
	jmp	PYRAMID3_300
PYRAMID3_200	cmp	#'2'
	bne	PYRAMID3_210
	jmp	PYRAMID3_700
PYRAMID3_210	cmp	#'3'
	bne	PYRAMID3_220
	jmp	PYRAMID3_700
PYRAMID3_220	cmp	#'4'
	bne	PYRAMID3_LOOP
	jmp	PYRAMID3_700

PYRAMID3_300	@PRINT	#0;PYRAMID3_str300
	@INKEY
	jmp	PYRAMID3_60

PYRAMID3_400	@PRINT	#0;PYRAMID3_str400
	@INKEY

	@PRINT	#0;PYRAMID3_str480
	lda	fgSPEECH
	beq	PYRAMID3_410
	@INKEY
	bra	PYRAMID3_420
	
PYRAMID3_410	@SPEECH	PYRAMID3_speech480
	@SPEECH	PYRAMID3_speech481

PYRAMID3_420	@PRINT	#0;PYRAMID3_str527
	lda	fgSPEECH
	beq	PYRAMID3_430
	@INKEY
	bra	PYRAMID3_440

PYRAMID3_430	@SPEECH	PYRAMID3_speech527
	@SPEECH	PYRAMID3_speech528
	@SPEECH	PYRAMID3_speech529
	@SPEECH	PYRAMID3_speech530

PYRAMID3_440	@PRINT	#0;PYRAMID3_str590
	@INKEY
	jmp	EPILOGUE

PYRAMID3_700	@PRINT	#0;PYRAMID3_str700
PYRAMID3_720	@INKEY
	cmp	#chrYES
	beq	PYRAMID3_760
	cmp	#chrNO
	bne	PYRAMID3_720
	jmp	PYRAMID3_60

PYRAMID3_760	@PRINT	#0;PYRAMID3_str760
PYRAMID3_770	@INKEY
	cmp	#chrYES
	beq	PYRAMID3_810
	cmp	#chrNO
	bne	PYRAMID3_770
	jmp	PYRAMID3_60

PYRAMID3_810	@PRINT	#0;PYRAMID3_str810
PYRAMID3_820	@INKEY
	cmp	#chrYES
	beq	PYRAMID3_860
	cmp	#chrNO
	bne	PYRAMID3_820

PYRAMID3_860	@PRINT	#0;PYRAMID3_str860
	@INKEY
	jmp	PYRAMID3_400

