	@PRINT	#1;DESERT2_str100
	@PRINT	#0;DESERT2_str200

DESERT2_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	DESERT2_200
	jmp	DESERT1
DESERT2_200	cmp	#'2'
	bne	DESERT2_210
	jmp	DESERT2_300
DESERT2_210	cmp	#'3'
	bne	DESERT2_220
	jmp	DESERT3
DESERT2_220	cmp	#'4'
	bne	DESERT2_230
	jmp	DESERT2_400
DESERT2_230	cmp	#'5'
	bne	DESERT2_240
	jmp	DESERT2_500
DESERT2_240	cmp	#'6'
	bne	DESERT2_250
	jmp	DESERT2_600
DESERT2_250	cmp	#'7'
	bne	DESERT2_260
	jmp	DESERT2_1000
DESERT2_260	cmp	#'8'
	bne	DESERT2_270
	jmp	DESERT2_1100
DESERT2_270	cmp	#'9'
	bne	DESERT2_LOOP
	jmp	DESERT2_1200

