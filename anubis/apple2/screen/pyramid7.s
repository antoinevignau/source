	ext	PYRAMID7_str100
	ext	PYRAMID7_str200
	ext	PYRAMID7_str300
	ext	PYRAMID7_str400
	ext	PYRAMID7_str500
	ext	PYRAMID7_str600
	ext	PYRAMID7_str700

PYRAMID7	@LOAD	#pPYRAMID7
PYRAMID7_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID7_str100
	@PRINT	#0;PYRAMID7_str200

PYRAMID7_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID7_200
	jmp	PYRAMID6
PYRAMID7_200	cmp	#'2'
	bne	PYRAMID7_210
	jmp	PYRAMID8
PYRAMID7_210	cmp	#'3'
	bne	PYRAMID7_220
	jmp	PYRAMID7_300
PYRAMID7_220	cmp	#'4'
	bne	PYRAMID7_230
	jmp	PYRAMID7_400
PYRAMID7_230	cmp	#'5'
	bne	PYRAMID7_240
	jmp	PYRAMID7_500
PYRAMID7_240	cmp	#'6'
	bne	PYRAMID7_250
	jmp	PYRAMID7_600
PYRAMID7_250	cmp	#'7'
	bne	PYRAMID7_LOOP	
	jmp	PYRAMID7_700

PYRAMID7_300	@PRINT	#0;PYRAMID7_str300
	@INKEY
	jmp	PYRAMID7_60

PYRAMID7_400	@PRINT	#0;PYRAMID7_str400
	@INKEY
	jmp	PYRAMIDB

PYRAMID7_500	@PRINT	#0;PYRAMID7_str500
	@INKEY
	jmp	PYRAMID7_60

PYRAMID7_600	@PRINT	#0;PYRAMID7_str600
	@INKEY
	jmp	PYRAMID7_60

PYRAMID7_700	@PRINT	#0;PYRAMID7_str700
	@INKEY
	jmp	PYRAMID7_60

