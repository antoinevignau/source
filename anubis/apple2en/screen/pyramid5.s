	ext	PYRAMID5_str100
	ext	PYRAMID5_str200
	ext	PYRAMID5_str600
	ext	PYRAMID5_str650
	ext	PYRAMID5_str700
	ext	PYRAMID5_str800
	ext	PYRAMID5_str900
	ext	PYRAMID5_str950
	ext	PYRAMID5_str1000

PYRAMID5	@LOAD	#pPYRAMID5
PYRAMID5_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID5_str100
	@PRINT	#0;PYRAMID5_str200

PYRAMID5_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID5_200
	jmp	PYRAMID4
PYRAMID5_200	cmp	#'2'
	bne	PYRAMID5_210
	jmp	PYRAMID6
PYRAMID5_210	cmp	#'3'
	bne	PYRAMID5_220
	jmp	PYRAMID9
PYRAMID5_220	cmp	#'4'
	bne	PYRAMID5_230
	jmp	PYRAMID5_600
PYRAMID5_230	cmp	#'5'
	bne	PYRAMID5_240
	jmp	PYRAMID5_700
PYRAMID5_240	cmp	#'6'
	bne	PYRAMID5_250
	jmp	PYRAMID5_800
PYRAMID5_250	cmp	#'7'
	bne	PYRAMID5_260
	jmp	PYRAMID5_900
PYRAMID5_260	cmp	#'8'
	bne	PYRAMID5_LOOP	
	jmp	PYRAMID5_1000

*---

PYRAMID5_600	@PRINT	#0;PYRAMID5_str600
	@INKEY
	@PRINT	#0;PYRAMID5_str650
	@INKEY
	jmp	PYRAMID5_60

PYRAMID5_700	@PRINT	#0;PYRAMID5_str700
	@INKEY
	jmp	MORT

PYRAMID5_800	@PRINT	#0;PYRAMID5_str800
	@INKEY
	jmp	PYRAMID5_60

PYRAMID5_900	@PRINT	#0;PYRAMID5_str900
	@INKEY
	@PRINT	#0;PYRAMID5_str950
	@INKEY
	jmp	PYRAMID5_60

PYRAMID5_1000	@PRINT	#0;PYRAMID5_str1000
	@INKEY
	jmp	PYRAMID5_60

*---

