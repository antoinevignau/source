DESERT3_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;DESERT3_str100
	@PRINT	#0;DESERT3_str200

DESERT3_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	DESERT3_200
	jmp	DESERT3_300
DESERT3_200	cmp	#'2'
	bne	DESERT3_210
	jmp	DESERT3_400
DESERT3_210	cmp	#'3'
	bne	DESERT3_220
	jmp	DESERT4
DESERT3_220	cmp	#'4'
	bne	DESERT3_230
	jmp	DESERT2
DESERT3_230	cmp	#'5'
	bne	DESERT3_240
	jmp	DESERT3_500
DESERT3_240	cmp	#'6'
	bne	DESERT3_250
	jmp	DESERT3_600
DESERT3_250	cmp	#'7'
	bne	DESERT3_260
	jmp	DESERT3_800
DESERT3_260	cmp	#'8'
	bne	DESERT3_LOOP
	jmp	DESERT3_900

