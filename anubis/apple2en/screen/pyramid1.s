	ext	PYRAMID1_str1700
	ext	PYRAMID1_str2000
	ext	PYRAMID1_str2100

PYRAMID1	@LOAD	#pPYRAMID1
PYRAMID1_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID1_str100
	@PRINT	#0;PYRAMID1_str200

PYRAMID1_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID1_200
	jmp	PYRAMID1_300
PYRAMID1_200	cmp	#'2'
	bne	PYRAMID1_210
	jmp	DESERT4
PYRAMID1_210	cmp	#'3'
	bne	PYRAMID1_220
	jmp	PYRAMID1_300
PYRAMID1_220	cmp	#'4'
	bne	PYRAMID1_230
	jmp	PYRAMID1_300
PYRAMID1_230	cmp	#'5'
	bne	PYRAMID1_240
	jmp	PYRAMID1_600
PYRAMID1_240	cmp	#'6'
	bne	PYRAMID1_250
	jmp	PYRAMID1_1100
PYRAMID1_250	cmp	#'7'
	bne	PYRAMID1_260
	jmp	PYRAMID1_400
PYRAMID1_260	cmp	#'8'
	bne	PYRAMID1_270
	jmp	PYRAMID1_1300	
PYRAMID1_270	cmp	#'9'
	bne	PYRAMID1_LOOP	
	jmp	PYRAMID1_500

PYRAMID1_300	@PRINT	#0;PYRAMID1_str300
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_400	@PRINT	#0;PYRAMID1_str400
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_500	@PRINT	#0;PYRAMID1_str500
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_600	lda	PEEK_7006
	cmp	#TRUE
	bne	PYRAMID1_610
	jmp	PYRAMID1_1700
	
PYRAMID1_610	@PRINT	#0;PYRAMID1_str600
	@INKEY
	
	lda	PEEK_7000
	cmp	#5
	bcs	PYRAMID1_700
	jmp	PYRAMID1_60

PYRAMID1_700	@PRINT	#0;PYRAMID1_str700
PYRAMID1_740	@INKEY
	cmp	#chrYES
	beq	PYRAMID1_800
	cmp	#chrNO
	bne	PYRAMID1_740
	jmp	PYRAMID1_60

PYRAMID1_800	@PRINT	#0;PYRAMID1_str800
PYRAMID1_880	@INKEY
	cmp	#'1'
	bcc	PYRAMID1_880
	cmp	#'7'
	bcs	PYRAMID1_880
	cmp	#'4'
	beq	PYRAMID1_1000

PYRAMID1_900	@PRINT	#0;PYRAMID1_str900
	@INKEY
	jmp	MORT

PYRAMID1_1000	@PRINT	#0;PYRAMID1_str1000
	@INKEY
	lda	#TRUE
	sta	PEEK_7006
	jmp	PYRAMID1_60

PYRAMID1_1100	@PRINT	#0;PYRAMID1_str1100
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_1300	lda	PEEK_7005
	cmp	#FALSE
	beq	PYRAMID1_1310
	cmp	#TRUE
	beq	PYRAMID1_1400

PYRAMID1_1310	@PRINT	#0;PYRAMID1_str1300
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_1400	lda	PEEK_7006
	cmp	#FALSE
	beq	PYRAMID1_1500
	cmp	#TRUE
	beq	PYRAMID1_1600

PYRAMID1_1500	@PRINT	#0;PYRAMID1_str1500
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_1600	@PRINT	#0;PYRAMID1_str1600
	@INKEY
	jmp	PYRAMID1_2000

PYRAMID1_1700	@PRINT	#0;PYRAMID1_str1700
	@INKEY
	jmp	PYRAMID1_60

PYRAMID1_2000	@PRINT	#0;PYRAMID1_str2000
	@INKEY

PYRAMID1_2100	@PRINT	#0;PYRAMID1_str2100
	@INKEY
	jsr	INIT_VARIABLES
	jmp	PYRAMID2

