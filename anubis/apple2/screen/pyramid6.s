	ext	PYRAMID6_str100
	ext	PYRAMID6_str200
	ext	PYRAMID6_str300
	ext	PYRAMID6_str400
	ext	PYRAMID6_str500
	ext	PYRAMID6_str600
	ext	PYRAMID6_str700

PYRAMID6	@LOAD	#pPYRAMID6
PYRAMID6_60	@SHOWPIC

	@WINDOW	#1;#theWINDOW1
	@PRINT	#1;PYRAMID6_str100
	@PRINT	#0;PYRAMID6_str200

PYRAMID6_LOOP	@INKEY
	@DEBUG
	cmp	#'1'
	bne	PYRAMID6_200
	jmp	PYRAMID5
PYRAMID6_200	cmp	#'2'
	bne	PYRAMID6_210
	jmp	PYRAMID7
PYRAMID6_210	cmp	#'3'
	bne	PYRAMID6_220
	jmp	PYRAMID2
PYRAMID6_220	cmp	#'4'
	bne	PYRAMID6_230
	jmp	PYRAMID6_300
PYRAMID6_230	cmp	#'5'
	bne	PYRAMID6_240
	jmp	PYRAMID6_400
PYRAMID6_240	cmp	#'6'
	bne	PYRAMID6_250
	jmp	PYRAMID6_500
PYRAMID6_250	cmp	#'7'
	bne	PYRAMID6_260
	jmp	PYRAMID6_600
PYRAMID6_260	cmp	#'8'
	bne	PYRAMID6_LOOP	
	jmp	PYRAMID6_700

*---
	
PYRAMID6_300	@PRINT	#0;PYRAMID6_str300
	@INKEY
	jmp	PYRAMID6_60

PYRAMID6_400	@PRINT	#0;PYRAMID6_str400
	@INKEY
	jmp	PYRAMID6_60

PYRAMID6_500	@PRINT	#0;PYRAMID6_str500
	@INKEY
	jmp	MORT

PYRAMID6_600	@PRINT	#0;PYRAMID6_str600
	@INKEY
	jmp	PYRAMID6_60

PYRAMID6_700	@PRINT	#0;PYRAMID6_str700
	@INKEY
	jmp	PYRAMID6_60

*---

